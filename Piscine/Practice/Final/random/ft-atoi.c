int	ft_atoi(const char *str)
{
    int i;
    int nb;
    int sign;
    
    i = 0;
    nb = 0;
    sign == 1;
    while (str[i] == 32 || (str[i] >= 9 && str[i] <= 14))
        i++;
    if (str[i] == '-')
    {
        sign = -1;
        i++;
    }
    else if (str[i] == '+')
        i++;
    while (str[i] != '\0' && str[i] >= '0' && str[i] <= '9')
    {
        nb = nb * 10 + str[i] - '0';
        i++;
    }
    return (nb * sign);
}